    private void OrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrderActionPerformed
        
        if(name.getText()!="" && address.getText()!=""){
        String cus_name = name.getText();
        String cus_num = number.getText();
        String cus_add = address.getText();
        
        double total = 0;
        
        double d_rate = 0;
        
        if(drinks1.isSelected()){
            d_rate = 80;
        }
        if(drinks2.isSelected()){
            d_rate += 80;
        }
        if(drinks3.isSelected()){
            d_rate += 50;
        }
        
        double sal = 0;
        if(sal1.isSelected()){
            sal = 120;
        }
        else if(sal2.isSelected()){
            sal = 140;
        }
        else if(sal3.isSelected()){
            sal = 160;
        }
        
        total = d_rate+sal+this.amount;
        
        field1.setText("Hello Mr "+cus_name+"\n"+"You Have Orderd "+this.item+"\n"+"Your Order No Is: "+cus_num+"\n"+"Will Be Deliver To: "+cus_add+"\n"+"You Have To Pay: "+total+"\n"+"Have Patience, Our Delivery Boy Will Be Touch You Soon \nThank You");
        
        
        AdminDash ads = new AdminDash();
        ads.getData(cus_name, cus_num, cus_add, (int) total);
        ads.addData();
        
        
        } else 
        
        {
            JOptionPane.showMessageDialog(null, "Please Enter Your Name & Address");
        }
        
        // User Name - name, User Number - number, U Address - address, total
        
        
    }//GEN-LAST:event_OrderActionPerformed

